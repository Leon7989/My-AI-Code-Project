CREATE TABLE admission_plan (
    id BIGINT NOT NULL AUTO_INCREMENT,
    public_id CHAR(26) NOT NULL,
    data_version_id BIGINT NOT NULL,
    exam_year INT NOT NULL,
    province_code VARCHAR(32) NOT NULL,
    university_id BIGINT NOT NULL,
    exam_regime VARCHAR(32) NOT NULL,
    plan_type VARCHAR(32) NOT NULL,
    subject_track VARCHAR(32) NULL,
    admission_batch_id BIGINT NOT NULL,
    special_program_type VARCHAR(64) NULL,
    plan_code VARCHAR(128) NOT NULL,
    total_quota INT NULL,
    status VARCHAR(32) NOT NULL,
    source_id BIGINT NULL,
    created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    PRIMARY KEY (id),
    UNIQUE KEY uk_admission_plan_public_id (public_id),
    UNIQUE KEY uk_admission_plan_version_university_code (data_version_id, university_id, plan_code),
    KEY idx_admission_plan_filter (data_version_id, exam_year, plan_type, subject_track, status),
    CONSTRAINT fk_admission_plan_data_version FOREIGN KEY (data_version_id) REFERENCES data_version (id),
    CONSTRAINT fk_admission_plan_university FOREIGN KEY (university_id) REFERENCES university (id),
    CONSTRAINT fk_admission_plan_batch FOREIGN KEY (admission_batch_id) REFERENCES admission_batch (id),
    CONSTRAINT fk_admission_plan_source FOREIGN KEY (source_id) REFERENCES policy_source (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

CREATE TABLE admission_plan_group (
    id BIGINT NOT NULL AUTO_INCREMENT,
    public_id CHAR(26) NOT NULL,
    admission_plan_id BIGINT NOT NULL,
    group_code VARCHAR(128) NULL,
    group_name VARCHAR(255) NULL,
    group_type VARCHAR(32) NOT NULL,
    first_choice_subject VARCHAR(32) NULL,
    subject_requirement_summary VARCHAR(1000) NULL,
    quota INT NULL,
    status VARCHAR(32) NOT NULL,
    source_id BIGINT NULL,
    created_at DATETIME(3) NOT NULL DEFAULT CURRENT_TIMESTAMP(3),
    PRIMARY KEY (id),
    UNIQUE KEY uk_admission_plan_group_public_id (public_id),
    UNIQUE KEY uk_admission_plan_group_code (admission_plan_id, group_code),
    CONSTRAINT fk_admission_plan_group_plan FOREIGN KEY (admission_plan_id) REFERENCES admission_plan (id),
    CONSTRAINT fk_admission_plan_group_source FOREIGN KEY (source_id) REFERENCES policy_source (id)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

